export const korWeekChange = engWeek => {
    switch (engWeek) {
        case 'mon':
            return '월';
        case 'tue':
            return '월';
        case 'wed':
            return '월';
        case 'thu':
            return '월';
        case 'fri':
            return '월';
        case 'sat':
            return '월';
        case 'sun':
            return '월';
        default:
            return engWeek;
    }
}